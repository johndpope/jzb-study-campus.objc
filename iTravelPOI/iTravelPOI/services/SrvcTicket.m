#import "SrvcTicket.h"


//*********************************************************************************************************************
#pragma mark -
#pragma mark SrvcTicket implementation
//---------------------------------------------------------------------------------------------------------------------
@implementation SrvcTicket


//---------------------------------------------------------------------------------------------------------------------
- (id)init {
    self = [super init];
    if (self) {
        _isCancelled = false;
    }
    return self;
}

//---------------------------------------------------------------------------------------------------------------------
- (void)dealloc
{
    [super dealloc];
}

//---------------------------------------------------------------------------------------------------------------------
- (void) cancel {
    _isCancelled = true;
}

//---------------------------------------------------------------------------------------------------------------------
- (BOOL) isCancelled {
    return _isCancelled;
}


@end
